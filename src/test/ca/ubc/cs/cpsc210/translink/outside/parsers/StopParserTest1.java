    private void testOnRoute(int stopNo, String routeNum) {
        Stop stop = StopManager.getInstance().getStopWithNumber(stopNo);
        Route route = RouteManager.getInstance().getRouteWithNumber(routeNum);
        assertTrue(stop.onRoute(route));
        assertTrue(route.hasStop(stop));
        for (Stop s : route.getStops())
            for (Route r : s.getRoutes()) {
                if (s.hashCode() == stop.hashCode())
                    assertEquals(s, stop);
                if (r.hashCode() == route.hashCode())
                    assertEquals(r, route);
            }

        assertFalse(StopManager.getInstance().getStopWithNumber(AutoTestConf.RND.nextInt()).onRoute(
                RouteManager.getInstance().getRouteWithNumber(AutoTestConf.RandomStringGen.generate(AutoTestConf.MAX_STR_LEN))));
    }
